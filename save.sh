save ()
{
  #now=$(date)
  now=$(date +"%d.%m.%Y_%H.%M.%S");

  if [[ -z $2 ]]; then
    #echo "Current date: $now";
    echo "-------------------------------------------" >> $now;
    echo $now "$1 commande" >> $now;
    echo "-------------------------------------------" >> $now;
    $1 >> $now;
  else
    echo "-------------------------------------------" >> $2;
    echo $now "$1 commande" >> $2;
    echo "-------------------------------------------" >> $2;
    $1 >> $2;
  fi
}
